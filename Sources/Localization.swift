import Foundation

enum AppLanguage: String, CaseIterable, Identifiable {
    case korean
    case english
    case japanese
    case simplifiedChinese
    case traditionalChinese
    case german
    case french
    case spanish
    case portugueseBrazil
    case italian
    case russian

    var id: String { rawValue }

    var titleKey: String {
        switch self {
        case .korean: return "menu.language.korean"
        case .english: return "menu.language.english"
        case .japanese: return "menu.language.japanese"
        case .simplifiedChinese: return "menu.language.simplifiedChinese"
        case .traditionalChinese: return "menu.language.traditionalChinese"
        case .german: return "menu.language.german"
        case .french: return "menu.language.french"
        case .spanish: return "menu.language.spanish"
        case .portugueseBrazil: return "menu.language.portugueseBrazil"
        case .italian: return "menu.language.italian"
        case .russian: return "menu.language.russian"
        }
    }
}

final class LocalizationController: ObservableObject {
    nonisolated(unsafe) static let shared = LocalizationController()

    @Published private(set) var selectedLanguage: AppLanguage?

    private let defaultsKey = "appLanguageOverride"

    private init() {
        let storedValue = UserDefaults.standard.string(forKey: defaultsKey)
        selectedLanguage = AppLanguage(rawValue: storedValue ?? "")
    }

    func setLanguage(_ language: AppLanguage) {
        guard selectedLanguage != language else { return }
        selectedLanguage = language
        UserDefaults.standard.set(language.rawValue, forKey: defaultsKey)
    }
}

enum Localized {
    static var currentLanguage: AppLanguage {
        LocalizationController.shared.selectedLanguage
            ?? inferredLanguage(from: Locale.preferredLanguages.first)
    }

    static func inferredLanguage(from preferredLanguage: String?) -> AppLanguage {
        let preferred = preferredLanguage?.lowercased() ?? "en"
        if preferred.hasPrefix("ko") {
            return .korean
        }
        if preferred.hasPrefix("ja") {
            return .japanese
        }
        if preferred.contains("hant") {
            return .traditionalChinese
        }
        if preferred.hasPrefix("zh") {
            return .simplifiedChinese
        }
        if preferred.hasPrefix("de") {
            return .german
        }
        if preferred.hasPrefix("fr") {
            return .french
        }
        if preferred.hasPrefix("es") {
            return .spanish
        }
        if preferred.hasPrefix("pt") {
            return .portugueseBrazil
        }
        if preferred.hasPrefix("it") {
            return .italian
        }
        if preferred.hasPrefix("ru") {
            return .russian
        }
        return .english
    }

    static func string(_ key: String) -> String {
        activeTable[key] ?? english[key] ?? key
    }

    static func string(_ key: String, _ arguments: CVarArg...) -> String {
        String(format: string(key), locale: Locale.current, arguments: arguments)
    }

    private static var activeTable: [String: String] {
        let language = currentLanguage

        switch language {
        case .korean: return korean
        case .english: return english
        case .japanese: return japanese
        case .simplifiedChinese: return simplifiedChinese
        case .traditionalChinese: return traditionalChinese
        case .german: return german
        case .french: return french
        case .spanish: return spanish
        case .portugueseBrazil: return portugueseBrazil
        case .italian: return italian
        case .russian: return russian
        }
    }

